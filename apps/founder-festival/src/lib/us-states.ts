export const US_COUNTRY_NAME = "United States";

// Display helpers — storage stays full-name (the dropdown writes "California",
// "United States"), but the location line on profiles is more readable as
// "City, CA, USA" than "City, California, United States".
const US_STATE_NAME_TO_CODE: Record<string, string> = Object.fromEntries(
  US_STATES.map((s) => [s.name, s.code]),
);

export function abbreviateRegion(region: string | null | undefined): string | null {
  if (!region) return null;
  const trimmed = region.trim();
  return US_STATE_NAME_TO_CODE[trimmed] ?? trimmed;
}

export function abbreviateCountry(country: string | null | undefined): string | null {
  if (!country) return null;
  const trimmed = country.trim();
  if (trimmed === US_COUNTRY_NAME) return "USA";
  return trimmed;
}
